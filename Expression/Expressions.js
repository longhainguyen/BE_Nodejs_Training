3 + 4;

//  x = 7 là một ví dụ cho loại thứ nhất. Biểu thức này sử dụng toán tử = để gán giá trị bảy cho biến x.
//  3 + 4 là một ví dụ cho loại thứ hai. Biểu thức này sử dụng toán tử + để cộng 3 và 4
